package com.sol.kx.web.dao.pojo;

public class CompareSupplyExcelProperty {
	private Integer startrow;
	private Integer pcode;
	private Integer pweight;
	private Integer shopname;
	private Integer dispname;
	private Integer serial;
	
	public Integer getStartrow() {
		return startrow;
	}
	public void setStartrow(Integer startrow) {
		this.startrow = startrow;
	}
	public Integer getPcode() {
		return pcode;
	}
	public void setPcode(Integer pcode) {
		this.pcode = pcode;
	}
	public Integer getPweight() {
		return pweight;
	}
	public void setPweight(Integer pweight) {
		this.pweight = pweight;
	}
	public Integer getShopname() {
		return shopname;
	}
	public void setShopname(Integer shopname) {
		this.shopname = shopname;
	}
	public Integer getDispname() {
		return dispname;
	}
	public void setDispname(Integer dispname) {
		this.dispname = dispname;
	}
	public Integer getSerial() {
		return serial;
	}
	public void setSerial(Integer serial) {
		this.serial = serial;
	}
}
